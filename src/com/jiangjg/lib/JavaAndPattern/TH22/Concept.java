package com.jiangjg.lib.JavaAndPattern.TH22;

public class Concept {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		/*
		 * 适配器模式
		 * 把一个类的接口编程客户端所期待的另一种接口
		 * 类的适配器模式：
		 * 目标角色(Target)：这就是所期待的接口
		 * 源(Adaptee)角色：现有需要适配的接口
		 * 适配器(Adapter)角色：把源接口转换成目标接口
		 * 
		 * 适用情况：
		 * 1. 系统需要使用现有的类，而此类的接口不符合系统的需要
		 * 2. 想要建立一个可以重复使用的类，用于与一些彼此之间没有太大关系的一些类，包括一些可能在将来引进的类一起工作
		 * 
		 * 缺省适配模式
		 * 就是抽象类实现接口，然后具体类实现抽象类，这样具体类就不用实现接口中的所有方法，抽象类提供了默认实现
		 */

	}

}
